#include "Skybox.hpp"
#include "Shader.hpp"
#include <stb_image.h>
#include <iostream>

Skybox::Skybox(const std::vector<std::string> &faces)
{
    setupSkybox();
    cubemapTexture = loadCubemap(faces);
}

Skybox::~Skybox()
{
    glDeleteVertexArrays(1, &VAO);
    glDeleteBuffers(1, &VBO);
    glDeleteTextures(1, &cubemapTexture);
}

